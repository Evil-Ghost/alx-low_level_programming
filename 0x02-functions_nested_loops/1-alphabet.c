#include "main.h"

/**
 * print_alphabet - prints lowercase alphabets and a new line
 */
void print_alphabet(void)
{
	char c;

	c = 'a';
	while (c <= 'z')
	{
		_putchar('c');
	}
}

/**
 * main - call the print_alphabet function
 *
 * Return: Always 0.
 */
int main(void)
{
	print_alphabet();
	return (0);
}
